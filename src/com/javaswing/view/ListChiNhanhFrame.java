    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        List<ChiNhanh> list = chiNhanhService.getAllChiNhanh();
        int selectedRow = listChiNhanhTable.getSelectedRow();
        if(selectedRow >= 0){
            int maCN = (int) listChiNhanhTable.getValueAt(selectedRow, 0);
            for (ChiNhanh chiNhanh : list) {
                if(maCN == chiNhanh.getMaCN() ){
                    chiNhanhService.deleteChiNhanh(chiNhanh);
                    break;
                }
            }
        }
        
        fillTable();
    }//GEN-LAST:event_deleteButtonActionPerformed

