    private void listChiNhanhTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listChiNhanhTableMouseClicked
        List<ChiNhanh> list = chiNhanhService.getAllChiNhanh();
        int selectedRow = listChiNhanhTable.getSelectedRow();
        if(selectedRow >= 0){
            int maCN = (int) listChiNhanhTable.getValueAt(selectedRow, 0);
            
            for (ChiNhanh chiNhanh : list) {
                if(maCN == chiNhanh.getMaCN()){
                    cnTextField.setText("" + chiNhanh.getMaCN());
                    tenCNTextField.setText(chiNhanh.getTenCN());
                    sdtTextField.setText("" + chiNhanh.getSdt());
                    diaChiTextField.setText(chiNhanh.getDiaChi());
                    break;
                }
            }
        }
    }//GEN-LAST:event_listChiNhanhTableMouseClicked

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        StringBuilder sb = new StringBuilder();

        if(tenCNTextField.getText().equals("")){
            sb.append("Ten chi nhanh khong duoc de trong! \n");
            tenCNTextField.setBackground(Color.red);
        } else {
            tenCNTextField.setBackground(Color.white);
        }
        if(diaChiTextField.getText().equals("")){
            sb.append("Dia chi cua chi nhanh khong duoc de trong! \n");
            diaChiTextField.setBackground(Color.red);
        } else {
            diaChiTextField.setBackground(Color.white);
        }
        if(sdtTextField.getText().equals("")){
            sb.append("SDT cua chi nhanh khong duoc de trong! \n");
            sdtTextField.setBackground(Color.red);
        } else {
            sdtTextField.setBackground(Color.white);
        }
        
        if(sb.length() > 0){
            JOptionPane.showMessageDialog(this, sb.toString(), "Error", 
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        List<ChiNhanh> list = chiNhanhService.getAllChiNhanh();
        int tempt = Integer.parseInt(cnTextField.getText());
        
        for (ChiNhanh cn : list) {
            if(cn.getMaCN() == tempt){
                chiNhanh.setMaCN(Integer.parseInt(cnTextField.getText()));
                chiNhanh.setTenCN(tenCNTextField.getText());
                chiNhanh.setDiaChi(diaChiTextField.getText());
                chiNhanh.setSdt(Integer.parseInt(sdtTextField.getText()));
                break;
            }
        }
        chiNhanhService.updateChiNhanh(chiNhanh);
        fillTable();
    }//GEN-LAST:event_updateButtonActionPerformed

