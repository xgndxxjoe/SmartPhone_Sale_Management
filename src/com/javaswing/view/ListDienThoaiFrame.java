    private void listDienThoaiTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listDienThoaiTableMouseClicked
        List<DienThoai> list = dienThoaiService.getAllDienThoai();
        int selectedRow = listDienThoaiTable.getSelectedRow();
        if(selectedRow >= 0){
            int maDT = (int) listDienThoaiTable.getValueAt(selectedRow, 0);
            
            for (DienThoai dt : list) {
                if(maDT == dt.getMaDT()){
                    maDTTextField.setText("" + dt.getMaDT());
                    tenDTTextField.setText(dt.getTenDT());
                    heDieuHanhTextField.setText(dt.getHeDieuHanh());
                    ramTextField.setText(dt.getRam());
                    boNhoTextField.setText(dt.getBoNho());
                    giaTextField.setText("" + dt.getGia());
                    soluongTextField.setText("" + dt.getSoLuong());
                    break;
                }
            }
        }
    }//GEN-LAST:event_listDienThoaiTableMouseClicked

