    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        List<DienThoai> list = dienThoaiService.getAllDienThoai();
        
        int selected = listDienThoaiTable.getSelectedRow();
        
        if(selected >= 0){
            int maDT = (int) listDienThoaiTable.getValueAt(selected, 0);
            
            for (DienThoai dt : list) {
                if(maDT == dt.getMaDT()){
                    dienThoaiService.deleteDienThoai(dienThoai);
                    break;
                }
            }
        }
        
        fillTable();
    }//GEN-LAST:event_deleteButtonActionPerformed

