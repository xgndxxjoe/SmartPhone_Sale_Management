    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        StringBuilder sb = new StringBuilder();
        if(maDTTextField.getText().equals("")){
            sb.append("Ma dien thoai khong duoc de trong! \n");
            maDTTextField.setBackground(Color.red);
        } else {
            maDTTextField.setBackground(Color.white);
        }
        if(tenDTTextField.getText().equals("")){
            sb.append("Ten dien thoai khong duoc de trong! \n");
            tenDTTextField.setBackground(Color.red);
        } else {
            tenDTTextField.setBackground(Color.white);
        }
        if(heDieuHanhTextField.getText().equals("")){
            sb.append("He dieu hanh cua dien thoai khong duoc de trong! \n");
            heDieuHanhTextField.setBackground(Color.red);
        } else {
            heDieuHanhTextField.setBackground(Color.white);
        }
        if(ramTextField.getText().equals("")){
            sb.append("RAM cua dien thoai khong duoc de trong! \n");
            ramTextField.setBackground(Color.red);
        } else {
            ramTextField.setBackground(Color.white);
        }
        if(boNhoTextField.getText().equals("")){
            sb.append("Bo nho cua dien thoai khong duoc de trong! \n");
            boNhoTextField.setBackground(Color.red);
        } else {
            boNhoTextField.setBackground(Color.white);
        }
        if(giaTextField.getText().equals("")){
            sb.append("Gia cua dien thoai khong duoc de trong! \n");
            giaTextField.setBackground(Color.red);
        } else {
            giaTextField.setBackground(Color.white);
        }
        if(soluongTextField.getText().equals("")){
            sb.append("So luong ton kho khong duoc de trong! \n");
            soluongTextField.setBackground(Color.red);
        } else {
            soluongTextField.setBackground(Color.white);
        }
        
        if(sb.length() > 0){
            JOptionPane.showMessageDialog(this, sb.toString(), "Error", 
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        List<DienThoai> list = dienThoaiService.getAllDienThoai();
        int maDT = Integer.parseInt(maDTTextField.getText());
        
        for (DienThoai dt : list) {
            if(dt.getMaDT() == maDT){
                dienThoai.setMaDT(Integer.parseInt(maDTTextField.getText()));
                dienThoai.setTenDT(tenDTTextField.getText());
                dienThoai.setHeDieuHanh(heDieuHanhTextField.getText());
                dienThoai.setRam(ramTextField.getText());
                dienThoai.setBoNho(heDieuHanhTextField.getText());
                dienThoai.setGia(Float.parseFloat(giaTextField.getText()));
                dienThoai.setSoLuong(Integer.parseInt(soluongTextField.getText()));
                break;
            }
        }
        dienThoaiService.updateDienThoai(dienThoai);
        fillTable();
    }//GEN-LAST:event_updateButtonActionPerformed

